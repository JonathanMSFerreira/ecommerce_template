import 'package:flutter/material.dart';


class CustomColors{



 static Color convert(String cor) {


    switch (cor) {

      case 'Azul':
        return Colors.blue;
      case 'Vermelha':
        return Colors.red;
      case 'Preta':
        return Colors.black;
      case 'Rosa':
        return Colors.pink;
      case 'Amarela':
        return Colors.yellow;
      case 'Branca':
        return Colors.white;
      case 'Verde':
        return Colors.green;
      case 'Cinza':
        return Colors.grey;
      case 'Marrom':
        return Colors.brown;
      case 'Laranja':
        return Colors.orange;
      case 'Roxo':
        return Colors.purple;


    }

  }













}